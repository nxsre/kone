package tcpip

import (
	"testing"
)

func TestChecksum(t *testing.T) {
	b := []byte{
		0x45, 0x00, 0x00, 0x73, 0x00, 0x00, 0x40, 0x00, 0x40, 0x11,
		0xb8, 0x61, 0xc0, 0xa8, 0x00, 0x01, 0xc0, 0xa8, 0x00, 0xc7,
	}

	expected := [2]byte{0x00, 0x00}
	v := Checksum(0, b)
	if v != expected {
		t.Errorf("v: %x", v)
	}
}
